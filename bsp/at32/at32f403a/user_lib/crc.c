uint16_t PbCrc16(uint8_t *updata, uint16_t len)
{
    uint16_t crc = 0xFFFF;

    while(len--)
    {
        crc = (crc >> 8) ^ TABLE[(crc ^ *updata++) & 0xff];
    }

    return  crc;
}


