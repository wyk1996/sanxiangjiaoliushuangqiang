/**
 * @brief CCITT 标准的CRC16，校验和
 * @param[in]   
 * @param[out] 
 * @return
 * @note 校验位长度为2 个字节，采用CCITT 标准的CRC16，其生成多项式为x16+x12+x5+1
 */
uint16_t GetCCITTCrc16(uint16_t crc_seed, const void *ptr, int len, uint8_t crc_in_text)
{
    uint8_t *buf = (uint8_t *)ptr;
    int i = 0;
    for (i = 0; i < len; i++)
        crc_seed = (crc_seed << 8) ^ crc_CCITT_tab[((crc_seed >> 8)^ *buf++) & 0xff];

	if(crc_in_text)
	{
		char c1,c2;
//		Crc16ToText(crc_seed, c1, c2) ;				//十六进制转单字节文本
		crc_seed = (((uint8_t)c1) << 8) | ((uint8_t)c2);
	}
    return crc_seed;
}

/**
 * @brief 与GPRS通讯的时候用这个CRC算法
 * @param[in]   
 * @param[out] 
 * @return
 * @note
 */
uint16_t GetModbusCrc16(uint16_t crc_seed,  const void *ptr, int len)	//bool crc_in_text
{
    uint8_t *buf = (uint8_t *)ptr;
    int i = 0;
    for (i = 0; i < len; i++)
        crc_seed = (crc_seed >> 8) ^ crc_16_tab[(crc_seed ^ *buf++) & 0xff];
    return crc_seed;
}

/**
 * @brief CCITT-XMODEM 标准的CRC16算法
 * @param[in] uint8_t*: crc计算数组起始指针 uiLen: 长度 uiInit:种子  
 * @param[out] 
 * @return uint16_t: crc值
 * @note 采用X16+X12+X5+1 余式表,种子为uiInit
 */
uint16_t LIB_CCITT_CRC(uint8_t* pucBuf, uint16_t uiLen, uint16_t usInit)
{
    uint8_t ucData;
    uint16_t usCRC = usInit;

    for(; uiLen>0; uiLen--)
    {
        ucData = (uint8_t) (usCRC >> 8);    			//暂存crc高8位
        usCRC <<= 8;
        usCRC ^= crc_CCITT_tab[ucData ^ (*pucBuf)];	//高字节和当前数据XOR再查表
        pucBuf++;
    }

    return usCRC;
}

/**
 * @brief 系统内部使用的crc校验,种子为0x5555
 * @param[in] pucBuf校验源指针, uiLen校验长度  
 * @param[out] 
 * @return
 * @note
 */
uint16_t LIB_InCRC(uint8_t* pucBuf, uint16_t usLen)
{
	return LIB_CCITT_CRC(pucBuf, usLen, 0x5555);
}

/**
 * @brief 重新计算接收到的数据包的CRC，如果计算得到的CRC与数据包中的CRC吻合
 * @param[in] cmd：指向待校验的数据缓存;len:待校验数据长度;cmp_crc:被比较的CRC值   
 * @param[out] 
 * @return
 * @note
 */
static char ret_buf[160];
uint8_t assert_crc16(char *cmd,uint16_t len,uint16_t cmp_crc)
{
	uint16_t crc16=0,i,true_len=0;		//true_len=转意前数据长度
	memset(ret_buf,0,sizeof(ret_buf));
	true_len = len;
	for(i=0;i<len;i++)					//重新计算CRC16
	{
		ret_buf[i]=cmd[i];
		if(cmd[i]=='\x1b')			//如果数据体中有0x02\0x03\0x1b的转意字节,则恢复成0x02\0x03\0x1b，		
		{
			if(cmd[i+1]=='A')		
			{
				ret_buf[i] = 0x02;
				i++;
				true_len --;			//注意：转意字节是两字节,因此真实数据长度减1
			}
			else if(cmd[i+1]=='B')
			{
				ret_buf[i] = 0x03;
				i++;
				true_len --;	
			}
			else if(cmd[i+1]=='C')
			{
				ret_buf[i] = 0x1b;
				i++;
				true_len --;	
			}	
		}
	}
	crc16 = GetModbusCrc16( 0xffff, ret_buf, true_len );	//收到命令包后重新计算CRC，已除去02,03及命令包中的CRC
	if(crc16==cmp_crc)	//计算得到的CRC与数据包中的CRC一致，则接收正确
	{
		memset(ret_buf,0,sizeof(ret_buf));
		return 1;
	}
	return 0;	
} 


