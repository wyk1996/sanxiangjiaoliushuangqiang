/**
 * @brief MODBUS专用CRC校验算法部分(高低字节分开计算)
 * @param[in] pucMsg:指向待校验的数据; usLen:待校验数据长度  
 * @param[out] 
 * @return
 * @note
 */
uint16_t LIB_Modbus_CRC(uint8_t *pucMsg, uint16_t usLen)
{
    uint8_t ucCRCHi = 0xff;
    uint8_t ucCRCLo = 0xff;
    uint16_t usIndex;
	if(usLen > 255)
	{
		return 0x0000;
	}
    
    while(usLen--)
    {
        usIndex = ucCRCHi ^ *pucMsg++;
        ucCRCHi = ucCRCLo ^ guc_CRCHi[usIndex];
        ucCRCLo = guc_CRCLo[usIndex];
    }
    
    return (((uint16_t)ucCRCHi << 8) | ucCRCLo);
}

