uint8_t GetCrc8(uint8_t crc_seed, const void *a_ptr, int len)
{
	int i=0;
	uint8_t crc;
	uint8_t *ptr = (uint8_t *)a_ptr;
	crc = crc_seed;
    for(i=0;i<len;i++)
		crc = crc8_table[crc ^ ptr[i]];
	return crc;
}

/**
 * @brief CRC8累加校验和
 * @param[in]   
 * @param[out] 
 * @return
 * @note
 */
uint8_t CheckSumCRC8(uint8_t *p, uint16_t usLen)
{
    uint8_t ucSum = 0;

    while(usLen--)
    {
        ucSum += *p;
		p ++;
    }
    
    return ucSum;
}

